#include<iostream>
using namespace std;

int main(){
  int angka;
  
  cout<< "contoh program perulangan ascending"<< endl;

  for(angka = 1; angka <= 10; angka++)
    cout<<angka<<endl;

  cout<<endl;

  cout<< " contoh program perulangan descending"<< endl;
  for(angka = 10; angka >= 1; angka--)
    cout<<angka<<endl;

  return 0;
  
  
}